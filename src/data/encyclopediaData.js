export function resolveEncyclopediaLocale(locale = 'en') {
  const normalized = `${locale}`.toLowerCase().replace(/_/g, '-')
  if (encyclopediaEntriesByLocale[normalized] || encyclopediaUiTextByLocale[normalized]) {
    return normalized
  }
  if (normalized.startsWith('zh-cn')) {
    return 'zh'
  }
  if (normalized.startsWith('zh-tw')) {
    return 'zh-tw'
  }
  const language = normalized.split('-')[0] || 'en'
  if (encyclopediaEntriesByLocale[language] || encyclopediaUiTextByLocale[language]) {
    return language
  }
  return 'en'
}
